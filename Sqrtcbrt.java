//here we are programming for the square root and the cube root 
class Sqrtcbrt{
    public static void main(String[] args) {
        double d=7.0;
        double Squareroot = Math.sqrt(d);
        double Cuberoot= Math.cbrt(d);
        System.out.println("square root is "+Squareroot);
        System.out.println("cube root is "+Cuberoot);
    }
}