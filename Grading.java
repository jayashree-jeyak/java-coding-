class Grading{
    public static void main(String[] args) {
        int n=88;
        if(n>90 && n<=100){
            System.out.println("A+");
        }
        else if(n>80 && n<=90){
            System.out.println("A");
        }
        else if(n>70 && n<=70){
            System.out.println("B+");
        }
        else if(n>60 && n<=60){
            System.out.println("B");
        }
    }
}